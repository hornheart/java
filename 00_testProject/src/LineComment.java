public class LineComment {
	public static void main(String[] args) {
		/*
		 * 주석
		 * 코드를 설명하거나, 의견을 메모할 때
		 * 코드에 영향을 주지 않고 작성할 수 있는 것
		 * (컴파일시 주석은 코드에서 자동으로 삭제된다.)
		 */
		// 한줄을 표현할 때는 // 내용
		/* 여러 줄을 표현할 때는 /* 내용 */
		/* 으로 작성하면 된다
		 */
		System.out.println(100); //단순한 정수 출력
		
		System.out.println("에헤라디야");
		System.out.println("올레이");
		
		
		System.out.println(19+95+5+14);
	}
}
